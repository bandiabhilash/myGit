class DayName 
{
	public static void main(String[] args) 
	{
		int day =6;
		switch(day)
		{
		case day1:System.out.println("sunday");break;
		case day2:System.out.println("monday");break;
		case day3:System.out.println("tuesday");break;
		case day4:System.out.println("wednesday");break;
		case day5:System.out.println("thursday");break;
		case day6:System.out.println("friday");break;
		case day7:System.out.println("saturday");break;
		default:System.out.println("invalid input!");
		}
	}
}
